public class ObjetoGeometrico {
    double ancho;
    double altura;

    public ObjetoGeometrico() {
        this.ancho = 1.0d;
        this.altura = 1.0d;
    }
    
    public ObjetoGeometrico(double ancho, double altura) {
        this.ancho = ancho;
        this.altura = altura;
    }
}
